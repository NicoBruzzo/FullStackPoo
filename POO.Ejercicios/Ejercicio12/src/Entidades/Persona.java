package Entidades;

import java.util.Date;

/**
 *
 * @author mcnar
 */
public class Persona {
    private String nombre;
    private int dia;
    private int mes;
    private int año;
    private Date nacimiento;

    public Persona() {
    }

    public Persona(String nombre, int dia, int mes, int año, Date nacimiento) {
        this.nombre = nombre;
        this.dia = dia;
        this.mes = mes;
        this.año = año;
        this.nacimiento = nacimiento;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAño() {
        return año;
    }

    public void setAño(int año) {
        this.año = año;
    }

    public Date getNacimiento() {
        return nacimiento;
    }

    public void setNacimiento(Date nacimiento) {
        this.nacimiento = nacimiento;
    }
    
}
